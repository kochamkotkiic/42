int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb == 1)
		return (1);
	while (i * i <= nb)
	{
		i ++;
	}
	if ((i - 1) * (i - 1) == nb)
		return (i - 1);
	return (0);
}

